
// Методы объекта Math над числами

console.log(Math.pow(2, 4)); // 2 в степени 4
console.log(Math.pow(3, 1/3)); // Кубический корень из 3
console.log(Math.round(.6)); // Округляет до ближайшего целого
console.log(Math.ceil(.3)); // Округляет в большую сторону до целого
console.log(Math.floor(.3)); // Округляет в меньшую сторону до целого
console.log(Math.abs(-5)); // Абсолютная величина
console.log(Math.max(3, -8, 23, 8, 65)); // Возвращает наибольший аргумент
console.log(Math.min(3, -8, 23, 8, 65)); // Возвращает наименьший аргумент
console.log(Math.random()); // Псевдослучайное число х, где 0 <= x , 1.0
console.log(Math.PI); // p: длина окружности деленая на диаметр
console.log(Math.E); // e: основание натурального логорифма
console.log(Math.sqrt(3)); // Квадратный корень из 3
console.log(Math.sin(0)); // Тригонометрия. Есть также cos, atan, и т.д.
console.log(Math.log(10)); // Натуральный логарифм 10
console.log(Math.log(100)/Math.LN10); // Десятичный логарифм 100
console.log(Math.log(512)/Math.LN2); // Двоичный логорифм 512
console.log(Math.exp(3)); // Math.E в кубе

console.log(0 / 0);
console.log(5 / 0);
console.log(8 * 0);